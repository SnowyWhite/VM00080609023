/**
 * This function generates a range of numbers from start to end with a specified step.
 *
 * @param {number} start - The starting number of the range.
 * @param {number} end - The ending number of the range.
 * @param {number} [step=1] - The step between each number in the range.
 * @returns {Generator} A generator object that yields each number in the range.
 */
function* range(start, end, step = 1) {
  let current = start;
  while (current <= end) {
    yield current;
    current += step;
  }
}

